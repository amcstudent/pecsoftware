    private void retrieveButtonMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_retrieveButtonMouseReleased
        if (retrieveButton.isEnabled()) {
            if (!helper.isConnected()) {
                JOptionPane.showMessageDialog(this,
                        "Please check your internet connection!",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
            } else {
                if (helper.emailValidity(emailText.getText().trim())) {
                    progressTask = new RetrieveAccount.ProgressTask();
                    progressTask.execute();
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Please enter a valid Email!",
                            "Error",
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_retrieveButtonMouseReleased

