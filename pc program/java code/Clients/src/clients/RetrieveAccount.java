    class ProgressTask extends SwingWorker<Void, Void> {

        @Override
        public Void doInBackground() throws ClassNotFoundException, SQLException, IOException, MessagingException {
            setCursor(Cursor.WAIT_CURSOR);
            waiting = true;
            retrieveButton.setEnabled(false);
            emailText.setEnabled(false);
            String urlString = helper.getDomainName() + "checkClientsByEmail.php?email=" + emailText.getText().trim();
            String result = helper.getMethod(urlString);
            if (result.trim().length() == 0) {
                JOptionPane.showMessageDialog(getContentPane(),
                        "The email doesn't exist!",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
            } else {
                String[] parts = result.split("\\$");
                String text = "Dear Sir,<br><br>Username : " + parts[0] + "<br><br>Password : " + parts[1];
                helper.sendEmail(emailText.getText().trim(), text, "PEC - Retrieve Account");
                JOptionPane.showMessageDialog(getContentPane(),
                        "An email has been sent to your mail!");
            }
            waiting = false;
            retrieveButton.setEnabled(true);
            emailText.setEnabled(true);
            setCursor(Cursor.DEFAULT_CURSOR);
            return null;
        }

        /*
     * Executed in event dispatching thread
         */
        @Override
        public void done() {

        }
    }

